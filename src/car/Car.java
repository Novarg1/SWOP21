package car;

public class Car {

	private Body body;
	private Color color;
	private Engine engine;
	private Gearbox gearbox;
	private Seats seats;
	private Wheels wheels;
	private Airco airco;
	private Spoiler spoiler;

	/**
	 * @return the body
	 */
	public Body getBody() {
		return body;
	}

	/**
	 * @param body
	 *            the body to set
	 */
	public void setBody(Body body) {
		this.body = body;
	}

	/**
	 * @return the color
	 */
	public Color getColor() {
		return color;
	}

	/**
	 * @param color
	 *            the color to set
	 */
	public void setColor(Color color) {
		this.color = color;
	}

	/**
	 * @return the engine
	 */
	public Engine getEngine() {
		return engine;
	}

	/**
	 * @param engine
	 *            the engine to set
	 */
	public void setEngine(Engine engine) {
		this.engine = engine;
	}

	/**
	 * @return the gearbox
	 */
	public Gearbox getGearbox() {
		return gearbox;
	}

	/**
	 * @param gearbox
	 *            the gearbox to set
	 */
	public void setGearbox(Gearbox gearbox) {
		this.gearbox = gearbox;
	}

	/**
	 * @return the seats
	 */
	public Seats getSeats() {
		return seats;
	}

	/**
	 * @param seats
	 *            the seats to set
	 */
	public void setSeats(Seats seats) {
		this.seats = seats;
	}

	/**
	 * @return the wheels
	 */
	public Wheels getWheels() {
		return wheels;
	}

	/**
	 * @param wheels
	 *            the wheels to set
	 */
	public void setWheels(Wheels wheels) {
		this.wheels = wheels;
	}

	/**
	 * @return the airco
	 */
	public Airco getAirco() {
		return airco;
	}

	/**
	 * @param airco
	 *            the airco to set
	 */
	public void setAirco(Airco airco) {
		this.airco = airco;
	}

	/**
	 * @return the spoiler
	 */
	public Spoiler getSpoiler() {
		return spoiler;
	}

	/**
	 * @param spoiler
	 *            the spoiler to set
	 */
	public void setSpoiler(Spoiler spoiler) {
		this.spoiler = spoiler;
	}

}
